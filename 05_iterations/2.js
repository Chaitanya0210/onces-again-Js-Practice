// while loop

let i=0

while(i<=10){
    console.log(i);
    i+=2
}

let arr = ['flash', 'batman', "superman"]

let j=0
while(j<arr.length){
    console.log(arr[j]);
    j++;
}

// do-while loop (in this case work is done atleast once before checking the condition)

let score = 1

do{
    score++
    console.log(score);
}while(score<10)